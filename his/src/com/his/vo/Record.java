package com.his.vo;

public class Record {
	
	private int rid;
	private String rname;
	private String rdesc;
	private String rview;
	private String rcure;
	private String rpresc;
	
	public int getRid() {
		return rid;
	}
	public void setRid(int rid) {
		this.rid = rid;
	}
	public String getRname() {
		return rname;
	}
	public void setRname(String rname) {
		this.rname = rname;
	}
	public String getRdesc() {
		return rdesc;
	}
	public void setRdesc(String rdesc) {
		this.rdesc = rdesc;
	}
	
	public String getRview() {
		return rview;
	}
	public void setRview(String rview) {
		this.rview = rview;
	}
	public String getRcure() {
		return rcure;
	}
	public void setRcure(String rcure) {
		this.rcure = rcure;
	}
	public String getRpresc() {
		return rpresc;
	}
	public void setRpresc(String rpresc) {
		this.rpresc = rpresc;
	}
	
	
}
